def closest_larger_multiple_of_minimum_size(size, minimum_size):
    return int(math.ceil(size / minimum_size) * minimum_size)

class ConvResidualBlocks(nn.Module):
    """Conv and residual blocks.

    Args:
        num_in_ch (int): Number of input channels. Default: 3.
        num_out_ch (int): Number of output channels. Default: 64.
        num_block (int): Number of residual blocks. Default: 15.
    """

    def __init__(self, num_in_ch=3, num_out_ch=64, num_block=15):
        super().__init__()
        self.main = nn.Sequential(
            nn.Conv2d(num_in_ch, num_out_ch, 3, 1, 1, bias=True), nn.LeakyReLU(negative_slope=0.1, inplace=True),
            make_layer(ResidualBlockNoBN, num_block, num_feat=num_out_ch))

    def forward(self, fea):
        return self.main(fea)


class TemporalAttention(nn.Module):
    def __init__(self, in_channels, time_channels):
        super().__init__()
        self.query_conv = nn.Conv2d(in_channels, in_channels, kernel_size=1)
        self.key_conv = nn.Conv2d(time_channels, in_channels, kernel_size=1)
        self.act_layer = nn.GELU()

    def forward(self, feat_prop, voxel):
        query = self.act_layer(self.query_conv(feat_prop))  # [B, dim, H, W]
        key = self.act_layer(self.key_conv(voxel))          # [B, dim, H, W]
        attention_scores = torch.sum(query * key, dim=[2, 3], keepdim=True)  # [B, dim, 1, 1]
        attention = F.softmax(attention_scores, dim=1)
        attended_feat_prop = attention * feat_prop
        output = attended_feat_prop + feat_prop
        return output

class FeatureAdjustment(nn.Module):
    def __init__(self, voxel_channels, in_channels):
        super().__init__()
        self.voxel_to_feature_conv = nn.Conv2d(voxel_channels, in_channels, kernel_size=1, bias=False)
        self.act_layer = nn.GELU()

    def forward(self, feat_prop, voxel_brightness):
        voxel_feature = self.act_layer(self.voxel_to_feature_conv(voxel_brightness))
        return feat_prop + voxel_feature

class EventFrameAlign(nn.Module):
    def __init__(self, in_channels, time_channels):
        super().__init__()
        self.temporal_attention = TemporalAttention(in_channels, time_channels)
        self.feature_adjustment = FeatureAdjustment(time_channels,in_channels)

    def forward(self, feat_prop, voxel):

        # Step 1: Apply temporal attention based on brightness change in voxel
        feat_temporal_attended = self.temporal_attention(feat_prop, voxel)

        # Step 2: Adjust features based on voxel brightness change
        aligned_feature = self.feature_adjustment(feat_temporal_attended, voxel)

        return aligned_feature